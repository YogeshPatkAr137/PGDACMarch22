class Circle12
{
public static void main(String args [])
{
double r = 7.5;
System.out.println("Radius of circle = "+r);
final double pi = 3.141592653589;


double a = pi*r*r;
double p = 2*pi*r;

System.out.println("Area of circle = "+a);
System.out.println("Perimeter of circle = "+p);

}
}