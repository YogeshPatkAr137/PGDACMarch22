class Perimeter13
{
public static void main(String args [])
{
double w = 5.6;
System.out.println("width of rectangle = "+w);
double h = 8.5;
System.out.println("Height of rectangle = "+h);

double a = w*h;
double p = 2*(w+h);

System.out.println("Area of Rectangle = "+a);
System.out.println("Perimeter of Rectangle = "+p);

}
}